// Color temperature set (§3.7.6). XX_L XX_H is LE 16-bit color temperature in K.
function buildColorTempCmd(kelvin) {
	const lo = kelvin & 0xff
	const hi = (kelvin >> 8) & 0xff
	return frame(0x55, 0xaa, 0x00, 0x00, 0xfe, 0xff, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x0d, 0x02, 0x05, 0x13, 0x02, 0x00, lo, hi)
}

// Screen volume set (§3.6.3). XX = 0x00-0x64.
function buildVolumeCmd(volume) {
	const v = Math.max(0, Math.min(100, volume | 0))
	return frame(0x55, 0xaa, 0x00, 0x00, 0xfe, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x10, 0x00, 0x02, 0x01, 0x00, v)
}

