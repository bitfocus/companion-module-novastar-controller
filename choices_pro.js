// VX2000 Pro is documented to have up to 12 layers; the smaller models
// typically address 1-4. We generate presets/actions for 12 across the
// board since the protocol supports it — the UI label just says "Layer N"
// and the user can pick any number within their device's actual layer count.
const PRO_LAYER_COUNT = 12

function buildLayerSourceChoices(sources) {
	const list = []
	for (let layer = 1; layer <= PRO_LAYER_COUNT; layer++) {
		for (const s of sources) {
			list.push({
				id: `L${layer}_${s.id}`,
				label: `Layer ${layer} - ${s.label}`,
				cmd: frame(0x55, 0xaa, 0x00, 0x3e, 0xfe, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x02, 0x13, 0x02, 0x00, layer, s.byte),
			})
		}
	}
	return list
}

