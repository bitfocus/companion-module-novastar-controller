// VX Pro Series protocol data (VX2000 Pro, VX1000 Pro, VX600 Pro, VX400 Pro).
// Spec: "VX Pro Series V1.0.5 Control Protocol" (NovaStar Tech Co., Ltd., 2025-10-10).
// Transport: TCP port 15200. Same `sum + 0x5555` little-endian checksum as the
// legacy VX/MCTRL family — so we can reuse the existing TCPHelper / sendMessage
// path. sendMessage strips the last 2 bytes and recomputes the checksum, so the
// trailing 2 bytes produced by `frame()` below are overwritten at send time but
// we still emit correct values here to keep the data self-describing.

/**
 * Compute the 16-bit little-endian checksum tail for a NovaStar command frame.
 * Matches getCommandChecksum() in index.js: sum(bytes[2..end]) + 0x5555, low byte first.
 */
function checksum(bytes) {
	let sum = 0
	for (let i = 2; i < bytes.length; i++) sum += bytes[i]
	sum += 0x5555
	return [sum & 0xff, (sum >> 8) & 0xff]
}

/** Assemble a full command frame from its prefix bytes, appending the checksum. */
function frame(...prefix) {
	const [lo, hi] = checksum(prefix)
	return Buffer.from([...prefix, lo, hi])
}

